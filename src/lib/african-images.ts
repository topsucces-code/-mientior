/**
 * Get a random image from a category
 */
export function getRandomImage(images: string[]): string {
  return images[Math.floor(Math.random() * images.length)] || ''
}

/**
 * Get product image based on category
 */
export function getProductImage(category: string, index: number = 0): string {
  const categoryMap: Record<string, string[]> = {
    'fashion': [...fashionImages.womensDresses, ...fashionImages.mensCasual],
    'women': fashionImages.womensDresses,
    'men': fashionImages.mensCasual,
    'dresses': fashionImages.womensDresses,
    'traditional': fashionImages.womensTraditional,
    'bags': fashionImages.bags,
    'jewelry': fashionImages.jewelry,
    'shoes': fashionImages.shoes,
    'electronics': [...electronicsImages.smartphones, ...electronicsImages.laptops],
    'phones': electronicsImages.smartphones,
    'laptops': electronicsImages.laptops,
    'headphones': electronicsImages.headphones,
    'watches': electronicsImages.watches,
    'home': [...lifestyleImages.homeDecor, ...lifestyleImages.kitchen],
    'beauty': lifestyleImages.beauty,
  }
  
  const images = categoryMap[category.toLowerCase()] || categoryMap['fashion'] || []
  return images[index % images.length] || ''
}

/**
 * Get testimonial avatar by index
 */
export function getTestimonialAvatar(index: number): string {
  return testimonialAvatars[index % testimonialAvatars.length] || ''
}

